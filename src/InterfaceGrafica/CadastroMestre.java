    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        
        String nomeMestre = campoMestre.getText();
        String nomeUsuario = campoUsuario.getText();
        String senha = campoSenha.getText();
        String senha2 = campoSenha2.getText();
        if (!senha.equals(senha2)){
            JOptionPane.showMessageDialog(null, "Senhas Diferentes",
                    "Mensagem de Erro", JOptionPane.ERROR_MESSAGE);
            campoSenha.setText("");
            campoSenha2.setText("");
        } else {
            try {
                Mestre m = new Mestre (nomeUsuario, senha);
                if(dao.adicionar(m) && !"".equals(campoUsuario.getText()) && !"".equals(campoSenha.getText())){
                    JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso");
                    LoginMestre novo = new LoginMestre();
                    novo.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Falha ao Salvar",
                    "Mensagem de Erro", JOptionPane.ERROR_MESSAGE);
                }
            } catch (IOException | ClassNotFoundException ex){
                JOptionPane.showMessageDialog(null, "Falha na conex√£o com o arquivo",
                    "Mensagem de Erro !", JOptionPane.ERROR_MESSAGE);
            } catch (NomeUsuarioDuplicadoException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(),
                    "Mensagem de Erro !", JOptionPane.ERROR_MESSAGE);
                campoUsuario.setText("");
                campoMestre.setText("");
                campoSenha.setText("");
                campoSenha2.setText("");
            }
        }
        
    }//GEN-LAST:event_salvarActionPerformed

